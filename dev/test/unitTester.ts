export class UnitTest {
    constructor(public name: string, public test: (s: string) => boolean) { }
}
export class UnitTestSet {
    constructor(public name: string, public tests: UnitTest[]) { }
}
export interface TestResult {
    name: string,
    passed: boolean,
    log: string
}

export class UnitTester {
    /** Hides constructor */
    private constructor() { }

    private static initializeTables(id: string) {
        const sheetapp = SpreadsheetApp.openById(id);

        sheetapp
            .insertSheet('Member')
            .appendRow([
                'id',
                'name',
                'dateJoined',
                'amountOwed',
                'email',
                'performing',
                'active',
                'officer',
                'currentDuesPaid',
                'notifyPoll',
                'sendReceipt'
            ])
            .setFrozenRows(1);
        sheetapp
            .insertSheet('Income')
            .appendRow(['id', 'date', 'amount', 'description', 'paymentTypeId', 'statementId'])
            .setFrozenRows(1);
        sheetapp
            .insertSheet('Expense')
            .appendRow([
                'id',
                'date',
                'amount',
                'description',
                'paymentTypeId',
                'recipientId',
                'statementId'
            ])
            .setFrozenRows(1);
        sheetapp.insertSheet('Recipient').appendRow(['id', 'name']).setFrozenRows(1);
        sheetapp.insertSheet('PaymentType').appendRow(['id', 'name']).setFrozenRows(1);
        sheetapp
            .insertSheet('Statement')
            .appendRow(['id', 'date', 'confirmed'])
            .setFrozenRows(1);
        sheetapp
            .insertSheet('Attendance')
            .appendRow(['id', 'date', 'memberIds', 'quarterId'])
            .setFrozenRows(1);
        sheetapp
            .insertSheet('ClubInfo')
            .appendRow([
                'memberFee',
                'officerFee',
                'daysUntilFeeRequired',
                'currentQuarterId'
            ]);

        sheetapp.deleteSheet(sheetapp.getSheetByName('Sheet1'));
    }

    public static runTests(tests: UnitTestSet[]) {
        const id = SpreadsheetApp.create('Test Database').getId().toString();

        this.initializeTables(id);

        let log: string[] = [];
        let totalPassed = 0;
        let totalTests = 0;

        tests.forEach(testSet => {
            for (const test of testSet.tests) {
                clearAllData(id);
                ++totalTests;
                try {
                    if (test.test(id)) {
                        ++totalPassed;
                    } else {
                        log.push(testSet.name + ' - ' + test.name + ': ' + 'Failed');
                    }
                } catch (e) {
                    log.push(testSet.name + ' - ' + test.name + ': ' + e);
                }
            }
        });

        // @ts-ignore Unable to find proper console.log
        console.log('Tests passed: ' + totalPassed + '/' + totalTests);
        if (log.length > 0) {
            // @ts-ignore Unable to find proper console.log
            console.log(log.join('\n'))
        }

        // @ts-ignore 'Drive.Files' is possibly undefined
        Drive.Files.trash(id);
    }
}